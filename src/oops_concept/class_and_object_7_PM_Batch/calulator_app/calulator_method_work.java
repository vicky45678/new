package oops_concept.class_and_object_7_PM_Batch.calulator_app;

public class calulator_method_work {

    public int add(int a, int b){

        int sum=a+b;
        return sum;

    }
    public int subtract(int a ,int b){

        int subtract=a-b;
        return subtract;
    }
    public int multiply(int a, int b){

        int multiply=a*b;
        return multiply;
    }
    public double divide(double a, double b){
        double divide=a/b;
        return  divide;

    }
    public double percentage(double a, double b){
        double percentage=a*b/100;
        return percentage;
    }

}
